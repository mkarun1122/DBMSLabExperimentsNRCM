-- SQL Queries
-- 1. Find employees working on at least one project (ANY) 
SELECT EID, FIRSTNAME, LASTNAME
FROM EMPLOYEE
WHERE EID = ANY
(SELECT EID FROM PROJECT);


-- 2. Find employees working on BIG DATA project (ALL)

SELECT EID, FIRSTNAME, LASTNAME
FROM EMPLOYEE
WHERE EID = ALL
(SELECT EID FROM PROJECT WHERE PNAME='BIG DATA');

-- 3. Employees from Delhi or Hyderabad (IN)
SELECT *
FROM EMPLOYEE
WHERE ADDRESS IN ('DELHI','HYDERABAD');

-- 4. Employees not from Hyderabad (NOT IN)
SELECT *
FROM EMPLOYEE
WHERE ADDRESS NOT IN ('HYDERABAD');

-- 5. Employees working on at least one project (EXISTS)

SELECT EID, FIRSTNAME, LASTNAME
FROM EMPLOYEE
WHERE EXISTS
(SELECT *
 FROM PROJECT
 WHERE EMPLOYEE.EID = PROJECT.EID);
 
 -- 6. Employees not working on any project (NOT EXISTS)
 
 SELECT EID, FIRSTNAME, LASTNAME
FROM EMPLOYEE
WHERE NOT EXISTS
(SELECT *
 FROM PROJECT
 WHERE EMPLOYEE.EID = PROJECT.EID);
 
