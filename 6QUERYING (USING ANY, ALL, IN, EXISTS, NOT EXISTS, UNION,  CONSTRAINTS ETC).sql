-- crate  PROJECT Table
CREATE TABLE PROJECT (
PID VARCHAR(5) PRIMARY KEY,
PNAME VARCHAR(20),
LOCATION VARCHAR(20),
EID VARCHAR(5),
FOREIGN KEY (EID) REFERENCES EMPLOYEE(EID)
); 

